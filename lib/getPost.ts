export async function getPostsByCategory(category: PostCategory): Promise<CardNineItemType[]> {
  return allPosts.filter((post) => post.category === category)
}

export async function getLatestPosts(page: number, limit: number): Promise<CardNineItemType[]> {
  return allPosts.slice(0, limit)
}

export function getPostById(postId: string): CardNineItemType | undefined {
  return allPosts.find((p) => p.id === postId)
}

export async function getPost(postId: string) {
  const byId = allPosts.find((p) => p.id === postId)
  if (byId) return byId
  const n = Number(postId)
  if (!Number.isNaN(n)) return allPosts[n % allPosts.length]
  return allPosts[0]
}
